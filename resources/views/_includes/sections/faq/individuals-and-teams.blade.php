	<p class="question">What is decentralized cloud storage?</p>
	<p class="answer">Decentralization allows Drive to provide a completely unique cloud storage service, where files are encrypted, fragmented and each fragmnent is hosted in servers present all over the world. This allows us to provide higher levels of privacy, eco-friendlyness and reliability. These servers are ran by high-quality operators running our exclusive Internxt Core program. Internxt Core is ran on already existing hardware, leveraging unused storage space, and thus providing a highly green cloud storage service. Since we count with storage servers everywhere in the globe, we also leverage latency to reduce energy consumption when sending you your files.</p>
